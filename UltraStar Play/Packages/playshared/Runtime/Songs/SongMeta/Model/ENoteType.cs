public enum ENoteType
{
    /**
     * Simple point weight, pitch is relevant
     */
    Normal,

    /**
     * Double point weight, pitch is relevant
     */
    Golden,

    /**
     * No point weight
     */
    Freestyle,

    /**
     * Simple point weight, pitch is irrelevant
     */
    Rap,

    /**
     * Double point weight, pitch is irrelevant
     */
    RapGolden
}
